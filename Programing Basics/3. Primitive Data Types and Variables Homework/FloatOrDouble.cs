using System;

namespace FloatOrDouble
{
    class FloatOrDouble
    {
        static void Main(string[] args)
        {
            double myDouble = 34.567839023;
            float myFloat = 12.345F;            
            double mynNextDouble = 8923.1234857;           
            float myNextFloat = 3456.091F;
            
            Console.WriteLine("{0}\r\n{1}\r\n{2}\r\n{3}",
                myDouble,
                myFloat,
                mynNextDouble,
                myNextFloat);   
        }
    }
}
